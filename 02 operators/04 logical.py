# operatory logiczne

print(True and True)
print(True and False)
print(False and True)
print(False and False) ; print(" ")
# operator and (i) obie strony muszą być prawdą zeby była prawda reszta fałsz

print(10>=5 and 3<9)
print(10>=5 and 3>9)
print(10==5 and 3>9)

print(" ") # odstęp
# operator or (lub) obie strony muszą być fałszem żeby był fałsz  reszta prawda

print(True or True) 
print(True or False)
print(False or True)
print(False or False) ; print(" ")

print(10>=5 or 3<9)
print(10>=5 or 3>9)
print(10==5 or 3>9); print(" ")

# not ofwraca wartość logiczną

print(10>=5 or 3<9)
print(not (10>=5 or 3<9))